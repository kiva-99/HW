# –ó–ê–î–ê–ù–ò–ï 1 ‚Äî –ê–Ω–∞–ª–∏–∑ –ª–æ–≥–æ–≤


logs = [
    "INFO: Service started",
    "ERROR: Connection timeout",
    "WARNING: CPU usage high",
    "ERROR: Disk full",
    "INFO: Health check passed",
    "ERROR: Out of memory"
]

# 1. –§–∏–ª—å—Ç—Ä—É–µ–º —Ç–æ–ª—å–∫–æ –æ—à–∏–±–∫–∏
error_logs = [log for log in logs if "ERROR" in log]

# 2. –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫
errors_count = len(error_logs)

# 3. –í—ã–≤–æ–¥–∏–º —Ä–µ–∑—É–ª—å—Ç–∞—Ç
print(f"   –ù–∞–π–¥–µ–Ω–æ –æ—à–∏–±–æ–∫: {errors_count}")
print(f"   –°–ø–∏—Å–æ–∫ –æ—à–∏–±–æ–∫: {error_logs}")

if errors_count > 2:
    print("–°—Ä–æ—á–Ω–æ —á–∏–Ω–∏–º –ø—Ä–æ–¥!")
else:
    print("–ü–æ–∫–∞ –¥–µ—Ä–∂–∏–º—Å—è")


# –ó–ê–î–ê–ù–ò–ï 2 ‚Äî –ú–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥ CPU

cpu_usage = [35, 55, 78, 90, 67, 120, 15]

# 1. –ù–∞—Ö–æ–¥–∏–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ
max_cpu = max(cpu_usage)

# 2. –°—á–∏—Ç–∞–µ–º —Å—Ä–µ–¥–Ω—é—é –∑–∞–≥—Ä—É–∑–∫—É
avg_cpu = sum(cpu_usage) / len(cpu_usage)

# 3. –ù–∞—Ö–æ–¥–∏–º –∑–Ω–∞—á–µ–Ω–∏—è –±–æ–ª—å—à–µ 80
high_load = [value for value in cpu_usage if value > 80]

# 4. –ü—Ä–æ–≤–µ—Ä–∫–∞ –Ω–∞ –Ω–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ (>100%)
is_invalid = any(value > 100 for value in cpu_usage)

print(f"   –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–∞—è –Ω–∞–≥—Ä—É–∑–∫–∞: {max_cpu}%")
print(f"   –°—Ä–µ–¥–Ω—è—è –Ω–∞–≥—Ä—É–∑–∫–∞: {avg_cpu:.2f}%")
print(f"   –ü–∏–∫–∏ –≤—ã—à–µ 80%: {high_load}")

if is_invalid:
    print("–ù–µ–∫–æ—Ä—Ä–µ–∫—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –º–æ–Ω–∏—Ç–æ—Ä–∏–Ω–≥–∞!")
else:
    print("–î–∞–Ω–Ω—ã–µ –≤ –Ω–æ—Ä–º–µ")


# –ó–ê–î–ê–ù–ò–ï 3 ‚Äî –†–∞–±–æ—Ç–∞ —Å —Å–µ—Ä–≤–µ—Ä–∞–º–∏




servers = [
    ("web-1", "online"),
    ("web-2", "offline"),
    ("db-1", "online"),
    ("cache-1", "offline"),
    ("cache-2", "offline")
]

# 1. –í—ã–≤–æ–¥–∏–º —Ç–æ–ª—å–∫–æ –∏–º–µ–Ω–∞ —Å–µ—Ä–≤–µ—Ä–æ–≤ —Å–æ —Å—Ç–∞—Ç—É—Å–æ–º "offline"
# –†–∞—Å–ø–∞–∫–æ–≤–∫–∞ –∫–æ—Ä—Ç–µ–∂–∞: for name, status in servers
offline_servers = [name for name, status in servers if status == "offline"]

# 2. –°—á–∏—Ç–∞–µ–º –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ–Ω–ª–∞–π–Ω —Å–µ—Ä–≤–µ—Ä–æ–≤
online_count = sum(1 for name, status in servers if status == "online")

print(f"–û—Ñ—Ñ–ª–∞–π–Ω —Å–µ—Ä–≤–µ—Ä—ã: {offline_servers}")
print(f"–û–Ω–ª–∞–π–Ω —Å–µ—Ä–≤–µ—Ä–æ–≤: {online_count}")

if online_count < 2:
    print("–†–∏—Å–∫ –ø–∞–¥–µ–Ω–∏—è —Å–µ—Ä–≤–∏—Å–∞!")
else:
    print("–°–µ—Ä–≤–∏—Å —Å—Ç–∞–±–∏–ª–µ–Ω")


# –ó–ê–î–ê–ù–ò–ï 4 ‚Äî –§—É–Ω–∫—Ü–∏—è –æ—Ç—á—ë—Ç–∞

print("\n –ì–µ–Ω–µ—Ä–∞—Ü–∏—è –æ—Ç—á—ë—Ç–∞")

def generate_report(errors_count, avg_cpu, offline_servers):
    """
    –§—É–Ω–∫—Ü–∏—è –ø—Ä–∏–Ω–∏–º–∞–µ—Ç –¥–∞–Ω–Ω—ã–µ –∏–∑ –ø—Ä–µ–¥—ã–¥—É—â–∏—Ö –∑–∞–¥–∞–Ω–∏–π
    –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –∫—Ä–∞—Å–∏–≤—É—é —Å—Ç—Ä–æ–∫—É –æ—Ç—á—ë—Ç–∞.
    
    –ê—Ä–≥—É–º–µ–Ω—Ç—ã:
    - errors_count: –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ –æ—à–∏–±–æ–∫ (int)
    - avg_cpu: —Å—Ä–µ–¥–Ω—è—è –∑–∞–≥—Ä—É–∑–∫–∞ CPU (float)
    - offline_servers: —Å–ø–∏—Å–æ–∫ –æ—Ñ—Ñ–ª–∞–π–Ω —Å–µ—Ä–≤–µ—Ä–æ–≤ (list)
    """
    # –ò—Å–ø–æ–ª—å–∑—É–µ–º f-—Å—Ç—Ä–æ–∫—É –¥–ª—è —Ñ–æ—Ä–º–∞—Ç–∏—Ä–æ–≤–∞–Ω–∏—è
    # :.2f ‚Äî –æ–∫—Ä—É–≥–ª–µ–Ω–∏–µ –¥–æ 2 –∑–Ω–∞–∫–æ–≤ –ø–æ—Å–ª–µ –∑–∞–ø—è—Ç–æ–π
    report = f"""
–û—à–∏–±–æ–∫ –≤ –ª–æ–≥–∞—Ö: {errors_count}
–°—Ä–µ–¥–Ω—è—è –∑–∞–≥—Ä—É–∑–∫–∞ CPU: {avg_cpu:.2f}%
–û—Ñ—Ñ–ª–∞–π–Ω —Å–µ—Ä–≤–µ—Ä—ã: {offline_servers}
"""
    return report

# --- –í–´–ó–û–í –§–£–ù–ö–¶–ò–ò ---
# –ü–µ—Ä–µ–¥–∞–µ–º –†–ï–ê–õ–¨–ù–´–ï –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ, –∫–æ—Ç–æ—Ä—ã–µ –º—ã –ø–æ—Å—á–∏—Ç–∞–ª–∏ –≤—ã—à–µ
final_report = generate_report(errors_count, avg_cpu, offline_servers)

# –í—ã–≤–æ–¥–∏–º –∏—Ç–æ–≥–æ–≤—ã–π –æ—Ç—á—ë—Ç
print(final_report)


