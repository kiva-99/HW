# task2_cpu.py

# 1. Исходные данные (загрузка процессора в процентах)
cpu_usage = [35, 55, 78, 90, 67, 120, 15]

# 2. Находим максимальное значение
max_cpu = max(cpu_usage)

# 3. Считаем среднюю загрузку
# sum() суммирует все числа, len() считает их количество
avg_cpu = sum(cpu_usage) / len(cpu_usage)

# 4. Находим значения больше 80 (критическая нагрузка)
high_load = [value for value in cpu_usage if value > 80]

# 5. Проверка на некорректные данные (больше 100%)
# any() возвращает True, если хотя бы один элемент в списке удовлетворяет условию
is_invalid = any(value > 100 for value in cpu_usage)

# Вывод результатов
print("Максимальная нагрузка:", max_cpu)
print("Средняя нагрузка:", avg_cpu)
print("Значения выше 80:", high_load)

if is_invalid:
    print("Некорректные данные мониторинга!")