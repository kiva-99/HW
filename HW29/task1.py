import math  # Импортируем модуль математических функций, чтобы взять число Пи

# 1. Создание класса "Круг"
class Circle:
    # Конструктор класса (специальный метод __init__)
    # Он вызывается автоматически при создании объекта
    def __init__(self, radius, color):
        # self указывает на текущий создаваемый объект
        # Мы сохраняем переданные значения в атрибуты объекта
        self.radius = radius
        self.color = color

    # Метод для вычисления площади (S = π * r^2)
    def get_area(self):
        # Используем атрибут self.radius, принадлежащий этому объекту
        area = math.pi * (self.radius ** 2)
        return area

    # Метод для вычисления длины окружности (L = 2 * π * r)
    def get_circumference(self):
        length = 2 * math.pi * self.radius
        return length

# Основная часть программы (здесь мы создаем объекты)
# Мы используем блок if __name__ == "__main__": для правильного запуска скрипта
if __name__ == "__main__":
    print("--- Задание 1: Класс Круг ---")
    
    # Нам нужно создать несколько объектов. Сделаем цикл для 2 объектов.
    # Чтобы код был простым, создадим 2 круга, как требует задание ("несколько объектов")
    circles = [] # Создаем пустой список, чтобы хранить наши круги
    
    count = 2 # Количество объектов для создания
    
    for i in range(count):
        print(f"\nСоздаем круг номер {i + 1}:")
        
        # ЗАПРОС ДАННЫХ У ПОЛЬЗОВАТЕЛЯ (не хардкодим!)
        # input() возвращает строку, поэтому радиус превращаем в число float()
        user_radius = float(input("Введите радиус круга: "))
        user_color = input("Введите цвет круга: ")
        
        # Создание объекта класса Circle
        # Здесь вызывается метод __init__
        new_circle = Circle(user_radius, user_color)
        
        # Добавляем объект в список
        circles.append(new_circle)

    # Теперь вызываем методы для каждого созданного объекта
    print("\n--- Результаты вычислений ---")
    for i, circle in enumerate(circles):
        print(f"\nОбъект Круг №{i + 1}:")
        print(f"Цвет: {circle.color}")
        print(f"Радиус: {circle.radius}")
        # Вызов методов объекта
        print(f"Площадь: {circle.get_area():.2f}") # :.2f означает округление до 2 знаков
        print(f"Длина окружности: {circle.get_circumference():.2f}")