# Задание 1: Функция умножения двух чисел

def multiply_numbers(a, b):
    """
    Функция принимает два аргумента и возвращает их произведение
    
    Параметры:
    a - первое число
    b - второе число
    
    Возвращает:
    произведение a и b
    """
    return a * b

# Вызываем функцию и выводим результат
result = multiply_numbers(5, 3)
print(f"Результат умножения: {result}")

# Проверим еще раз с другими числами
result2 = multiply_numbers(7, 4)
print(f"Результат умножения 7 на 4: {result2}")