# Запрашиваем первую строку у пользователя
string1 = input("Введите первую строку: ")

# Запрашиваем вторую строку
string2 = input("Введите вторую строку: ")

# Создаём множества символов из обеих строк
# Множество (set) автоматически удаляет дубликаты
set1 = set(string1)
set2 = set(string2)

# Находим пересечение множеств — символы, которые есть в ОБЕИХ строках
common_chars = set1 & set2  # или: set1.intersection(set2)

# Выводим результат
print("\nОбщие символы в обеих строках:")
if common_chars:
    # Сортируем для красивого вывода (необязательно, но удобно)
    for char in sorted(common_chars):
        # Показываем символ и его "код" для наглядности (особенно для пробелов/табуляций)
        if char == ' ':
            display = "' ' (пробел)"
        elif char == '\t':
            display = "'\\t' (табуляция)"
        elif char == '\n':
            display = "'\\n' (новая строка)"
        else:
            display = f"'{char}'"
        print(f"  - {display}")
else:
    print("  Общих символов нет")