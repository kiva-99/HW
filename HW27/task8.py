# Запрашиваем числа у пользователя
numbers_str = input("Введите числа через пробел: ")

# Преобразуем строку в список чисел
numbers = [float(num) for num in numbers_str.split()]  # float вместо int — чтобы работало с дробями

# Сортируем список по возрастанию
numbers.sort()

# Находим длину списка
n = len(numbers)

# Вычисляем медиану
if n % 2 == 1:  # Нечётное количество (остаток от деления на 2 = 1)
    # Индекс среднего элемента: (n - 1) // 2
    # Пример: n=5 → (5-1)//2 = 2 → третий элемент (индексы: 0,1,2,3,4)
    median = numbers[(n - 1) // 2]
else:  # Чётное количество
    # Два средних элемента: по индексам (n//2 - 1) и (n//2)
    # Пример: n=4 → индексы 1 и 2 → второй и третий элементы
    mid1 = numbers[n // 2 - 1]
    mid2 = numbers[n // 2]
    median = (mid1 + mid2) / 2

# Выводим результат
print("\nОтсортированный список:")
print(numbers)
print(f"\nМедиана: {median}")