# Запрашиваем у пользователя элементы кортежа через пробел
elements_str = input("Введите элементы кортежа через пробел: ")

# Преобразуем строку в кортеж
# 1. split() — разбиваем на список строк: "яблоко груша" → ['яблоко', 'груша']
# 2. tuple() — преобразуем список в кортеж: ('яблоко', 'груша')
my_tuple = tuple(elements_str.split())

# Проверяем уникальность элементов
# Множество (set) хранит ТОЛЬКО уникальные элементы
# Если длина кортежа == длина множества — все элементы уникальны
if len(my_tuple) == len(set(my_tuple)):
    result = "Все элементы уникальны"
else:
    result = "Есть повторяющиеся элементы"

# Выводим результат
print("\nВведённый кортеж:")
print(my_tuple)
print(f"\nРезультат проверки: {result}")