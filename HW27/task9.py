# Определяем все гласные буквы (и русские, и английские, в обоих регистрах)
vowels = "ауоиэыяюёеАОИЭЫЯЮЁЕaeiouAEIOU"

# Запрашиваем строку у пользователя
text = input("Введите строку: ")

# Создаём пустую строку для результата
result = ""

# Перебираем каждый символ в исходной строке
for char in text:
    # Проверяем: является ли символ гласной буквой?
    if char in vowels:
        result += "-"  # Заменяем гласную на "-"
    else:
        result += char  # Оставляем символ без изменений

# Выводим результат
print("\nРезультат замены гласных на '-':")
print(result)