# Запрашиваем первый список у пользователя
# Пользователь вводит элементы через пробел: яблоко груша 123 @
list1_str = input("Введите первый список (элементы через пробел): ")

# Запрашиваем второй список
list2_str = input("Введите второй список (элементы через пробел): ")

# Преобразуем строки в списки с помощью .split()
# split() без параметров разбивает по пробелам (любое количество)
list1 = list1_str.split()
list2 = list2_str.split()

# Создаём пустой список для общих элементов
common_elements = []

# Перебираем каждый элемент из первого списка
for element in list1:
    # Проверяем: есть ли этот элемент во втором списке?
    if element in list2:
        # Если да — добавляем в список общих элементов
        common_elements.append(element)

# Выводим результаты
print("\nОбщие элементы в обоих списках:")
if common_elements:  # Если список не пустой
    for elem in common_elements:
        print(f"  - {elem}")
else:
    print("  Нет общих элементов")
